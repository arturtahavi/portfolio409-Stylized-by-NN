function quote(){
    // Генерируем случайный индекс
    let quoteIndex = Math.floor(Math.random() * quotes.length);
    
    // Отображаем текст цитаты
    document.getElementById('quoteText').innerHTML = '"' + quotes[quoteIndex] + '"';
    
    // Показываем номер цитаты
    document.getElementById('quoteIndex').textContent = (quoteIndex + 1).toString().padStart(2, '0');
    
    // Формируем путь к изображению
    let path;
    if (quoteIndex < 10) {
        path = "static/img/0" + quoteIndex + ".jpg";
    } else {
        path = "static/img/" + quoteIndex + ".jpg";
    }
    
    // Устанавливаем изображение
    const imgElement = document.getElementById('quotePic');
    imgElement.src = path;
    imgElement.alt = "Иллюстрация к цитате #" + (quoteIndex + 1);
    
    // Обновляем текст кнопки
    document.getElementById('quoteButton').innerHTML = '<i class="fas fa-sync-alt"></i> Еще цитата';
    
    console.log("Цитата #" + (quoteIndex + 1));
}

